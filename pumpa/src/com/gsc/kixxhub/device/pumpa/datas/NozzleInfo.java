package com.gsc.kixxhub.device.pumpa.datas;

/**
 * @author GSC
 * 
 */
public class NozzleInfo {

	String basePrice = "";
	String ID = "";
	String nozzleNo = "";
	String oilCode = "";
	String oilName = "";

	/**
	 * @return the basePrice
	 */
	public String getBasePrice() {
		return basePrice;
	}

	/**
	 * @return the iD
	 */
	public String getID() {
		return ID;
	}

	/**
	 * @return the nozzleNo
	 */
	public String getNozzleNo() {
		return nozzleNo;
	}

	/**
	 * @return the oilCode
	 */
	public String getOilCode() {
		return oilCode;
	}

	/**
	 * @return the oilName
	 */
	public String getOilName() {
		return oilName;
	}

	/**
	 * @param basePrice
	 *            the basePrice to set
	 */
	public void setBasePrice(String basePrice) {
		this.basePrice = basePrice;
	}

	/**
	 * @param id
	 *            the iD to set
	 */
	public void setID(String id) {
		ID = id;
	}

	/**
	 * @param nozzleNo
	 *            the nozzleNo to set
	 */
	public void setNozzleNo(String nozzleNo) {
		this.nozzleNo = nozzleNo;
	}

	/**
	 * @param oilCode
	 *            the oilCode to set
	 */
	public void setOilCode(String oilCode) {
		this.oilCode = oilCode;
	}

	/**
	 * @param oilName
	 *            the oilName to set
	 */
	public void setOilName(String oilName) {
		this.oilName = oilName;
	}

}
